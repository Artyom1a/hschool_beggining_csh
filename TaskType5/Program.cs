// // // 1. Выведите столбец чисел от 1 до 50. for, while
// // // 
{
    Console.WriteLine("Цикл for");
    for (int ind = 1; ind <= 50; ind++)
    {
        Console.WriteLine(ind);
    }
    int number = 1;
    Console.WriteLine("Цикл while");
    while (number < 50)
    {
        Console.WriteLine(number++);
    }
}